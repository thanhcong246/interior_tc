package com.vn.tcshop.foodapp.Models;

public class Product_detail_specifications {
    private String width;
    private String height;
    private String number_of_drawers;
    private String type;

    public String getWidth() {
        return width;
    }

    public void setWidth(String width) {
        this.width = width;
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getNumber_of_drawers() {
        return number_of_drawers;
    }

    public void setNumber_of_drawers(String number_of_drawers) {
        this.number_of_drawers = number_of_drawers;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
