package com.vn.tcshop.foodapp.Models;

public class Notification {
    private String title;
    private String date_time;
    private String email;
    private String notifications_s;

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDate_time() {
        return date_time;
    }

    public void setDate_time(String date_time) {
        this.date_time = date_time;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getNotifications_s() {
        return notifications_s;
    }

    public void setNotifications_s(String notifications_s) {
        this.notifications_s = notifications_s;
    }
}
